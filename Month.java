/*||||||||||||||||||||||||||||||||||||||||
  MONTH CLASS
  Allows for the creation of obejects of
  type Month. Months have a days and month
  variable to them
  ||||||||||||||||||||||||||||||||||||||*/

public class Month {

    //========================
    //===INSTANCE VARIABLES===
    //========================
    private int maxDays;
    private int numMonth;

    //==================
    //===CONSTRUCTORS===
    //==================
    public Month( int max, int num ) {
	maxDays = max;
	numMonth = num;
    }
}
